fn main() {
    
    let a = 23;  // 불변 변수

    let mut b = 23;  // 가변 변수

    let c: i32 = 23;  // 정수형

    let d: f64 = 11.1;  // 실수형

    let e: char = 'A';  // 문자형

    let f: bool = true;  // 불리언

    const G: i32 = 100;

    let x = 12;
    let x = x + 1;

    println!("{a}");
    println!("{b}");
    println!("{c}");
    println!("{d}");
    println!("{e}");
    println!("{f}");
    println!("{G}");
    println!("{x}");

}